    private void limpiarFormCategoria(){
        modeloTablaCategoria.setRowCount(0);
        modeloTablaCategoria.setColumnCount(0);
        txtName.setText("");
        txtDescription.setText("");
        CargarColumnasCategoria();
        lblId.setText(""+0);
        lblEditando.setVisible(false);
    }
    
